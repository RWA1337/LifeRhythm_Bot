def chunk_and_send(chat_id: int, text: str, max_len: int = 3500):
    """–î–µ–ª–∏—Ç –±–æ–ª—å—à–æ–π —Ç–µ–∫—Å—Ç –Ω–∞ —á–∞—Å—Ç–∏ –∏ –æ—Ç–ø—Ä–∞–≤–ª—è–µ—Ç –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ (–ª–∏–º–∏—Ç Telegram ‚âà4096)."""
    parts = []
    while len(text) > max_len:
        # —Å—Ç–∞—Ä–∞–µ–º—Å—è —Ä–µ–∑–∞—Ç—å –ø–æ –≥—Ä–∞–Ω–∏—Ü–µ –∞–±–∑–∞—Ü–∞/—Å—Ç—Ä–æ–∫–∏
        cut = text.rfind("\n", 0, max_len)
        if cut == -1:
            cut = max_len
        parts.append(text[:cut])
        text = text[cut:]
    if text:
        parts.append(text)
    for p in parts:
        bot.send_message(chat_id, p)

